void ssd_init(FemuCtrl *n)
{
    struct ssd *ssd = n->ssd;
    struct ssdparams *spp = &ssd->sp;

    ftl_assert(ssd);

    ssd_init_params(spp);
    struct nand_thread nand_init[spp->nchs];

    /* initialize ssd internal layout architecture */
    ssd->ch = g_malloc0(sizeof(struct ssd_channel) * spp->nchs);
    for (int i = 0; i < spp->nchs; i++) {
        nand_init[i].sp = *spp;
        nand_init[i].ch_idx=i;
        nand_init[i].ch=&ssd->ch[i];
        qemu_thread_create(&nand_init[i].nand_init, "FEMU-NAND-Thread", allocate_ssd_nand, &nand_init[i],
                       QEMU_THREAD_JOINABLE);
    }

    /* initialize maptbl */
    ssd_init_maptbl(ssd);

    /* initialize rmap */
    ssd_init_rmap(ssd);

    /* initialize all the lines */
    ssd_init_lines(ssd);

    /* initialize write pointer, this is how we allocate new pages for writes */
    ssd_init_write_pointer(ssd);

    while(t<spp->nchs){
        usleep(100000);
    }

    qemu_set_log(CPU_LOG_TB_OUT_ASM);
    error_f_path = g_build_filename(dir1, "ace_error.log", NULL);
    vol_f_path = g_build_filename(dir2, "ace_voltage.log", NULL);

    qemu_thread_create(&ssd->ftl_thread, "FEMU-FTL-Thread", ftl_thread, n,
                       QEMU_THREAD_JOINABLE);
    femu_log("End ssd initialize... Start ACE...\n");
}

static inline bool valid_ppa(struct ssd *ssd, struct ppa *ppa)
{
    struct ssdparams *spp = &ssd->sp;
    int ch = ppa->g.ch;
    int lun = ppa->g.lun;
    int pl = ppa->g.pl;
    int blk = ppa->g.blk;
    int pg = ppa->g.pg;
    int sec = ppa->g.sec;

    if (ch >= 0 && ch < spp->nchs && lun >= 0 && lun < spp->luns_per_ch && pl >=
        0 && pl < spp->pls_per_lun && blk >= 0 && blk < spp->blks_per_pl && pg
        >= 0 && pg < spp->pgs_per_blk && sec >= 0 && sec < spp->secs_per_pg)
        return true;

    return false;
}

