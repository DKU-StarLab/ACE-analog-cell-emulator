static int t = 0;
static void* allocate_ssd_nand(void *arg){
    struct nand_thread *n = (struct nand_thread *)arg;
    struct ssdparams *spp = &n->sp;
    int i = n->ch_idx;
    struct ssd_channel *ch = n->ch;

    femu_log("allocate %d ch start\n",i);
    ch->nluns = spp->luns_per_ch;
    ch->lun = g_malloc0(sizeof(struct nand_lun) * ch->nluns);
    for (int i = 0; i < ch->nluns; i++) {
        ssd_init_nand_lun(&ch->lun[i], spp);
    }
    femu_log("allocate ch %d done\n",i);
     ch->next_ch_avail_time = 0;
     ch->busy = 0;
     t++;

    qemu_thread_exit(NULL);
    return NULL;
}

